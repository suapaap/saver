package day02;

public class 별모양_연습 {

	public static void main(String[] args) {
		/* 별찍기
		 * *****
		 * *****
		 * *****
		 * *****
		 * *****
		 * */
		for(int i=0; i<=5; i++) {
			for(int j=0; j<=5; j++) {
				System.out.print('*');
			}
			System.out.println();
		}
		
		/* *
		 * **
		 * ***
		 * ****
		 * *****
		 * */
		
	}
}
